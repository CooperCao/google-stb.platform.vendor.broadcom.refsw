#ifndef _BB_ZBPRO_ZCL_HANDLER_PROFILE_WIDE_COMMON_H
#define _BB_ZBPRO_ZCL_HANDLER_PROFILE_WIDE_COMMON_H


/************************* INCLUDES *****************************************************/
#include "private/bbZbProZclDispatcher.h"


/************************* DEFINITIONS **************************************************/
/**//**
 * \brief   Enumeration of profile-wide ZCL commands.
 * \details
 *  These commands may be generated by both client and server sides of all clusters, for
 *  ZCL Standard commands domain as well as for Manufacturer Specific domain.
 * \note
 *  Only the following commands are implemented:
 *  - Read attributes,
 *  - Read attributes response,
 *  - Write attributes,
 *  - Write attributes response,
 *  - Default response.
 *
 *  This implementation is not able to generate or receive and process the rest of
 *  profile-wide commands.
 * \par     Documentation
 *  See ZigBee Document 075123r05, subclause 2.4, table 2-2.
 */
enum
{
    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_READ_ATTR                         = 0x00,     /*!< Read attributes. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_READ_ATTR_RESP                    = 0x01,     /*!< Read attributes response. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_WRITE_ATTR                        = 0x02,     /*!< Write attributes. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_WRITE_ATTR_UNDIVIDED              = 0x03,     /*!< Write attributes undivided. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_WRITE_ATTR_RESP                   = 0x04,     /*!< Write attributes response. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_WRITE_ATTR_NO_RESP                = 0x05,     /*!< Write attributes no response. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_CONFIGURE_REPORTING               = 0x06,     /*!< Configure reporting. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_CONFIGURE_REPORTING_RESP          = 0x07,     /*!< Configure reporting response. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_READ_REPORTING_CONFIGURATION      = 0x08,     /*!< Read reporting configuration. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_READ_REPORTING_CONFIGURATION_RESP = 0x09,     /*!< Read reporting configuration
                                                                                    response. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_REPORT_ATTRIBUTES                 = 0x0A,     /*!< Report attributes. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_DEFAULT_RESP                      = 0x0B,     /*!< Default response. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_DISCOVER_ATTR                     = 0x0C,     /*!< Discover attributes. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_DISCOVER_ATTR_RESP                = 0x0D,     /*!< Discover attributes response. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_READ_ATTR_STRUCTURED              = 0x0E,     /*!< Read attributes structured. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_WRITE_ATTR_STRUCTURED             = 0x0F,     /*!< Write attributes structured. */

    ZBPRO_ZCL_PROFILE_WIDE_CMD_ID_WRITE_ATTR_STRUCTURED_RESP        = 0x10,     /*!< Write attributes structured
                                                                                    response. */
};


#endif /* _BB_ZBPRO_ZCL_HANDLER_PROFILE_WIDE_COMMON_H */
