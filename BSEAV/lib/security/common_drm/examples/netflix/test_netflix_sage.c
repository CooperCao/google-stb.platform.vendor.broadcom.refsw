    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Decrypt() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("%s:%d - DRM_Netflix_Decrypt() success with size %d\n",BSTD_FUNCTION,__LINE__,outSize);

    if( memcmp( clearBuf,pClearBuf,sizeof(clearBuf)) != 0)
    {
        printf("%s:%d - DRM_Netflix_Decrypt() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }

    /********************************************************************/
    /*                         HMAC Verify                                                                     */
    /********************************************************************/

    rc = DRM_Netflix_Import_Key((DrmNetFlixSageHandle)netflixSageCtx,
                             test_hmac_key,sizeof(test_hmac_key),
                             0, /*RAW format*/
                             0, /*nfCA_HMAC*/
                             0xC,
                             &keyhandle,  /*out*/
                             &keyTypePtr);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Key_Info() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("%s:%d - DRM_Netflix_Import_Key Succeeded \n",BSTD_FUNCTION,__LINE__);

    rc = DRM_Netflix_Get_Key_Info((DrmNetFlixSageHandle)netflixSageCtx,
                               keyhandle,
                               &keyTypePtr,        /*out*/
                               &extractable,       /*out*/
                               &algorithmPtr,      /*out*/
                               &keyUsageFlagsPtr);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Key_Info() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }

    rc = DRM_Netflix_HMAC_Verify((DrmNetFlixSageHandle)netflixSageCtx,
                                        keyhandle,9,
                                        test_hmac,sizeof(test_hmac),
                                        test_hmac_data,sizeof(test_hmac_data),
                                        &verified);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_HMAC_Verify() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    if(verified == true)
        printf("%s:%d - DRM_Netflix_HMAC_Verify verification passed\n",BSTD_FUNCTION,__LINE__);
    else
        printf("%s:%d - DRM_Netflix_HMAC_Verify verification failed\n",BSTD_FUNCTION,__LINE__);

    /********************************************************************/
    /*                         DKE key info                                                                    */
    /********************************************************************/

    namedKeySize = 3;/**/
    rc = DRM_Netflix_Get_Named_Key((DrmNetFlixSageHandle)netflixSageCtx,
                                namedKeyPtr,
                                namedKeySize,
                                &keyhandle);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Named_Key() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("%s:%d - DRM_Netflix_Get_Named_Key %c%c%c KeyHandle 0x%x ",BSTD_FUNCTION,__LINE__,
                                namedKeyPtr[0],namedKeyPtr[1],namedKeyPtr[2],keyhandle);

    rc = DRM_Netflix_Get_Key_Info((DrmNetFlixSageHandle)netflixSageCtx,
                               keyhandle,
                               &keyTypePtr,        /*out*/
                               &extractable,       /*out*/
                               &algorithmPtr,      /*out*/
                               &keyUsageFlagsPtr);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Key_Info() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("keyType 0x%x algorithm 0x%x keyUsageFlags 0x%x \n", keyTypePtr,algorithmPtr, keyUsageFlagsPtr);


    /********************************************************************/
    /*                         DKH key info                                                                    */
    /********************************************************************/
    namedKeyPtr[2]='H';
    rc = DRM_Netflix_Get_Named_Key((DrmNetFlixSageHandle)netflixSageCtx,
                                namedKeyPtr,
                                namedKeySize,
                                &keyhandle);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Named_Key() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("%s:%d - DRM_Netflix_Get_Named_Key %c%c%c KeyHandle 0x%x ",BSTD_FUNCTION,__LINE__,
                                namedKeyPtr[0],namedKeyPtr[1],namedKeyPtr[2],keyhandle);
    rc = DRM_Netflix_Get_Key_Info((DrmNetFlixSageHandle)netflixSageCtx,
                               keyhandle,
                               &keyTypePtr,        /*out*/
                               &extractable,       /*out*/
                               &algorithmPtr,      /*out*/
                               &keyUsageFlagsPtr);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Key_Info() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("keyType 0x%x algorithm 0x%x keyUsageFlags 0x%x \n", keyTypePtr,algorithmPtr, keyUsageFlagsPtr);

    /********************************************************************/
    /*                         DKW key info                                                                    */
    /********************************************************************/

    namedKeyPtr[2]='W';/*DKW key*/
    rc = DRM_Netflix_Get_Named_Key((DrmNetFlixSageHandle)netflixSageCtx,
                                namedKeyPtr,
                                namedKeySize,
                                &keyhandle);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Named_Key() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("%s:%d - DRM_Netflix_Get_Named_Key %c%c%c KeyHandle 0x%x ",BSTD_FUNCTION,__LINE__,
                                namedKeyPtr[0],namedKeyPtr[1],namedKeyPtr[2],keyhandle);
    rc = DRM_Netflix_Get_Key_Info((DrmNetFlixSageHandle)netflixSageCtx,
                               keyhandle,
                               &keyTypePtr,        /*out*/
                               &extractable,       /*out*/
                               &algorithmPtr,      /*out*/
                               &keyUsageFlagsPtr);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Key_Info() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("keyType 0x%x algorithm 0x%x keyUsageFlags 0x%x \n", keyTypePtr,algorithmPtr, keyUsageFlagsPtr);

    /********************************************************************/
    /*                         DH_Gen_Keys                                                                  */
    /********************************************************************/
    rc = DRM_Netflix_DH_Gen_Keys((DrmNetFlixSageHandle)netflixSageCtx,
                                 generatorPtr,
                                 sizeof(generatorPtr),
                                 primePtr,
                                 sizeof(primePtr),
                                 &pubkeyHandle,
                                 &privkeyHandle);

    rc = DRM_Netflix_Get_Key_Info((DrmNetFlixSageHandle)netflixSageCtx,
                               pubkeyHandle,
                               &keyTypePtr,        /*out*/
                               &extractable,       /*out*/
                               &algorithmPtr,      /*out*/
                               &keyUsageFlagsPtr);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Key_Info() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("%s:%d - DRM_Netflix_DH_Gen_Keys pub KeyHandle 0x%x keyType 0x%x algorithm 0x%x keyUsageFlags 0x%x \n",
                                BSTD_FUNCTION,__LINE__,pubkeyHandle,keyTypePtr,algorithmPtr,keyUsageFlagsPtr);

    rc = DRM_Netflix_Get_Key_Info((DrmNetFlixSageHandle)netflixSageCtx,
                               privkeyHandle,
                               &keyTypePtr,        /*out*/
                               &extractable,       /*out*/
                               &algorithmPtr,      /*out*/
                               &keyUsageFlagsPtr);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Key_Info() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("%s:%d - DRM_Netflix_DH_Gen_Keys prv KeyHandle 0x%x keyType 0x%x algorithm 0x%x keyUsageFlags 0x%x \n",
                                BSTD_FUNCTION,__LINE__,privkeyHandle,keyTypePtr,algorithmPtr, keyUsageFlagsPtr);

    /********************************************************************/
    /*                         Export KEY                                                                  */
    /********************************************************************/

    keySizePtr = sizeof(keyDataPtr);
    rc = DRM_Netflix_Export_Key((DrmNetFlixSageHandle)netflixSageCtx,
                            pubkeyHandle,
                            0,
                            keyDataPtr,
                            &keySizePtr);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Export_Key() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }

    printf("%s:%d - DRM_Netflix_Export_Key Succeeded. KeySize %d #### \n",BSTD_FUNCTION,__LINE__,keySizePtr);

    /********************************************************************/
    /*                         AES CBC Computation                                                       */
    /********************************************************************/

    keySizePtr = 16;
    rc = DRM_Netflix_Import_Key((DrmNetFlixSageHandle)netflixSageCtx,
                             aesKey,
                             sizeof(aesKey),
                             0x0, /*RAW format*/
                             0x1, /*AES_CBC Algorithm*/
                             0x3, /*KeyUsage*/
                             &keyhandle, /*out*/
                             &keySizePtr);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Get_Key_Info() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }

    outDataSize = sizeof(Test_outDataPtr);
    rc = DRM_Netflix_AES_CBC((DrmNetFlixSageHandle)netflixSageCtx,
                              keyhandle,
                              0, /*Encrypt*/
                              ivPtr,
                              sizeof(ivPtr),
                              inDataPtr,
                              sizeof(inDataPtr),
                              Test_outDataPtr,   /*out*/
                              &outDataSize);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_AES_CBC() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }

    for(i=0;i<outDataSize;i++)
    {
        if(Test_outDataPtr[i] != outDataPtr[i])
        {
            printf("DRM_Netflix_AES_CBC Computation Failed \n");
            goto ErrExit;
        }
    }
    printf("%s:%d - DRM_Netflix_AES_CBC Computation Passed \n",BSTD_FUNCTION,__LINE__);

    rc = DRM_Netflix_Delete_Key((DrmNetFlixSageHandle)netflixSageCtx,
                                    keyhandle);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Delete_Key() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }

    /********************************************************************/
    /*                             HMAC Computation                                                       */
    /********************************************************************/

    keySizePtr = 32;
    rc = DRM_Netflix_Import_Key((DrmNetFlixSageHandle)netflixSageCtx,
                             HmacKey,
                             sizeof(HmacKey),
                             0x0, /*RAW format*/
                             0x0, /*HMAC Algorithm*/
                             0xc, /*KeyUsage*/
                             &keyhandle, /*out*/
                             &keySizePtr);
    if( rc != Drm_Success) {
        printf("%s:%d - HmacKey DRM_Netflix_Import_Key() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }

    keySizePtr = 32;
    rc = DRM_Netflix_HMAC((DrmNetFlixSageHandle)netflixSageCtx,
                            keyhandle,9,
                            HmacInDataPtr,sizeof(HmacInDataPtr),
                            keyDataPtr,&keySizePtr);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_HMAC() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }

    for(i=0;i<keySizePtr;i++)
    {
        if(keyDataPtr[i] != HmacOutDataPtr[i])
        {
            printf("DRM_Netflix_HMAC Computation Failed \n");
            goto ErrExit;
        }
    }
    printf("%s:%d - DRM_Netflix_HMAC Computation Passed \n",BSTD_FUNCTION,__LINE__);

    rc = DRM_Netflix_Delete_Key((DrmNetFlixSageHandle)netflixSageCtx,
                                    keyhandle);
    if( rc != Drm_Success) {
        printf("%s:%d - DRM_Netflix_Delete_Key() failed\n",BSTD_FUNCTION,__LINE__);
        goto ErrExit;
    }
    printf("%s:%d - DRM_Netflix_Delete_Key Succeeded \n",BSTD_FUNCTION,__LINE__);


ErrExit:

    if(pStr != NULL)
    {
        NEXUS_Memory_Free(pStr);
        pStr = NULL;
    }
    if(pEncBuf != NULL)
    {
        NEXUS_Memory_Free(pEncBuf);
        pEncBuf = NULL;
    }
    if(pClearBuf != NULL)
    {
        NEXUS_Memory_Free(pClearBuf);
        pClearBuf = NULL;
    }
    if(pOutBuf != NULL)
    {
        NEXUS_Memory_Free(pOutBuf);
        pOutBuf = NULL;
    }

    DRM_Netflix_Finalize((DrmNetFlixSageHandle)netflixSageCtx);
    NEXUS_Platform_Uninit();
    printf("\n\tMAIN - Done\n");
    return 0;
