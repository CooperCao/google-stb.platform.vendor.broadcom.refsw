#define CONVERT(g_struct) \
    unsigned i; \
    for (i=0;i<sizeof(g_struct)/sizeof(g_struct[0]);i++) { \
        if (g_struct[i].bmedia == bmedia_value) { \
            return g_struct[i].nexus; \
        } \
    } \
    BDBG_ERR(("unable to find BMedia value %d in %s", bmedia_value, #g_struct)); \
    return g_struct[0].nexus

/* bmedia_types.h still uses some bsettop names for transport types */
#define CONVERT_BSETTOP(g_struct) \
    unsigned i; \
    for (i=0;i<sizeof(g_struct)/sizeof(g_struct[0]);i++) { \
        if (g_struct[i].settop == settop_value) { \
            return g_struct[i].nexus; \
        } \
    } \
    printf("unable to find value %d in %s\n", settop_value, #g_struct); \
    return g_struct[0].nexus

#define CONVERT_BWIN(g_struct) \
    unsigned i; \
    for (i=0;i<sizeof(g_struct)/sizeof(g_struct[0]);i++) { \
        if (g_struct[i].bwin == bwin_value) { \
            return g_struct[i].nexus; \
        } \
    } \
    printf("unable to find value %d in %s\n", bwin_value, #g_struct); \
    return g_struct[0].nexus

NEXUS_TransportType b_mpegtype2nexus(bstream_mpeg_type settop_value)     { CONVERT_BSETTOP(g_mpegType); }
NEXUS_AudioCodec    bmediaToAudioCodec(baudio_format bmedia_value)       { CONVERT(g_audioCodec); }
NEXUS_VideoCodec    bmediaToVideoCodec(bvideo_codec bmedia_value)        { CONVERT(g_videoCodec); }
NEXUS_PixelFormat   bwinToNexusPixelFormat(bwin_pixel_format bwin_value) { CONVERT_BWIN(g_pixelFormat); }

NEXUS_Rect bwinToNexusRect(bwin_rect brect)
{
    NEXUS_Rect nrect;
    nrect.x      = brect.x;
    nrect.y      = brect.y;
    nrect.width  = brect.width;
    nrect.height = brect.height;

    return(nrect);
}

