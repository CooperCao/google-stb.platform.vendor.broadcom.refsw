void print_usage(void) {
    printf("Usage:\n");
    printf("nexus i2s_input\n");
    printf("-decode - decode the i2s input\n");
    printf("-dual - decode and also pass through the i2s input\n");
}

NEXUS_PlatformConfiguration platformConfig;

int main(int argc, char **argv)
{
    NEXUS_Error errCode;
    NEXUS_I2sInputHandle i2sInput;
    NEXUS_I2sInputSettings i2sInputSettings;
    NEXUS_AudioCapabilities audioCapabilities;
    NEXUS_AudioOutputHandle audioDacHandle = NULL;
    NEXUS_AudioOutputHandle i2sOutputHandle = NULL;
    NEXUS_AudioOutputHandle spdifHandle = NULL;
    NEXUS_AudioDecoderHandle audioDecoder = NULL;
    NEXUS_AudioInputCaptureHandle inputCapture = NULL;
    NEXUS_AudioMixerHandle intMixer = NULL;
#if NEXUS_HAS_HDMI_OUTPUT
    NEXUS_AudioOutputHandle audioHdmiHandle = NULL;
    NEXUS_DisplayHandle display;
    NEXUS_DisplaySettings displaySettings;
    NEXUS_HdmiOutputStatus hdmiStatus;
    NEXUS_HdmiOutputSettings hdmiSettings;
    hotplugCallbackParameters hotPlugCbParams;
#endif
    NEXUS_TimebaseSettings timebaseSettings;
    bool decode = false;
    bool dual = false;
    int curarg = 1;
    unsigned samplerate = 48000;
    unsigned bitspersample = 32;
    bool aligned = false;
    bool lsbfirst = false;

    while (curarg < argc) {
        if (!strcmp("--help", argv[curarg]) ||
            !strcmp("-h", argv[curarg]) ||
            !strcmp("-?", argv[curarg])) {
            print_usage();
            return 1;
        }
        else if ( !strcmp(argv[curarg], "-decode" )) {
            decode = true;
        }
        else if ( !strcmp(argv[curarg], "-dual") ) {
            dual = true;
        }
        else if ( !strcmp(argv[curarg], "-sr") && (curarg+1) < argc ) {
            switch (atoi(argv[curarg+1]))
            {
            case 48000:
            case 44100:
            case 32000:
            case 24000:
            case 22050:
            case 16000:
                samplerate = atoi(argv[curarg+1]);
                break;
            default:
                printf("Unknown samplerate: %s\n", argv[curarg+1]);
                return -1;
            }
            curarg++;
        }
        else if ( !strcmp(argv[curarg], "-bps") && (curarg+1) < argc ) {
            switch (atoi(argv[curarg+1]))
            {
            case 32:
            case 24:
            case 16:
                bitspersample = atoi(argv[curarg+1]);
                break;
            default:
                printf("Unknown bits per sample: %s\n", argv[curarg+1]);
                return -1;
            }
            curarg++;
        }
        else if ( !strcmp(argv[curarg], "-mode") && (curarg+1) < argc ) {
            if ( strcmp(argv[curarg + 1], "left") )
            {
                aligned = true;
                lsbfirst = false;
            }
            else if ( strcmp(argv[curarg + 1], "right") )
            {
                aligned = true;
                lsbfirst = true;
            }
            else /* default - i2s mode */
            {
                aligned = false;
                lsbfirst = false;
            }
            curarg++;
        }
        else {
            printf("Unknown param: %s\n", argv[curarg]);
            return -1;
        }
        curarg++;
    }
    NEXUS_Platform_Init(NULL);
    NEXUS_Platform_GetConfiguration(&platformConfig);
    NEXUS_GetAudioCapabilities(&audioCapabilities);

    if (audioCapabilities.numInputs.i2s == 0)
    {
        printf("This application is not supported on this platform.\n");
        return 0;
    }

    if (audioCapabilities.numOutputs.dac > 0)
    {
        audioDacHandle = NEXUS_AudioDac_GetConnector(platformConfig.outputs.audioDacs[0]);
    }
    if (audioCapabilities.numOutputs.i2s > 0)
    {
        i2sOutputHandle = NEXUS_I2sOutput_GetConnector(platformConfig.outputs.i2s[0]);
    }
    if (audioCapabilities.numOutputs.spdif > 0)
    {
        spdifHandle = NEXUS_SpdifOutput_GetConnector(platformConfig.outputs.spdif[0]);
    }
    #if NEXUS_HAS_HDMI_OUTPUT
    if (audioCapabilities.numOutputs.hdmi > 0)
    {
        audioHdmiHandle = NEXUS_HdmiOutput_GetAudioConnector(platformConfig.outputs.hdmi[0]);
    }
    #endif

    i2sInput = NEXUS_I2sInput_Open(0, NULL);
    if ( NULL == i2sInput )
    {
        fprintf(stderr, "Unable to open i2s input 0\n");
        return -1;
    }

    NEXUS_I2sInput_GetSettings(i2sInput, &i2sInputSettings);
    i2sInputSettings.bitsPerSample = bitspersample;
    i2sInputSettings.sampleRate = samplerate;
    i2sInputSettings.alignedToLRClock = aligned;
    i2sInputSettings.lsbAtLRClock = lsbfirst;
    errCode = NEXUS_I2sInput_SetSettings(i2sInput, &i2sInputSettings);
    if ( errCode ) {
        fprintf(stderr, "Unable to connect Decoder -> SPDIF Output\n");
        goto cleanup;
    }

    /* Bring up video display and outputs */
#if NEXUS_HAS_HDMI_OUTPUT
    NEXUS_Display_GetDefaultSettings(&displaySettings);
    NEXUS_HdmiOutput_GetStatus(platformConfig.outputs.hdmi[0], &hdmiStatus);
    if ( hdmiStatus.connected ) {
        displaySettings.format = hdmiStatus.preferredVideoFormat;
    }
    display = NEXUS_Display_Open(0, &displaySettings);
    NEXUS_HdmiOutput_GetSettings(platformConfig.outputs.hdmi[0], &hdmiSettings);
    hdmiSettings.hotplugCallback.callback = hotplug_callback;
    hotPlugCbParams.hdmiOutput = platformConfig.outputs.hdmi[0];
    hotPlugCbParams.display = display;
    hdmiSettings.hotplugCallback.context = &hotPlugCbParams;
    NEXUS_HdmiOutput_SetSettings(platformConfig.outputs.hdmi[0], &hdmiSettings);

    /* initalize HDCP settings, keys, etc. */
    initializeHdmiOutputHdcpSettings() ;

    /* Force a hotplug to switch to preferred format */
    hotplug_callback(&hotPlugCbParams, 0);
    NEXUS_Display_AddOutput(display, NEXUS_HdmiOutput_GetVideoConnector(platformConfig.outputs.hdmi[0]));
#endif

    /* set up the decode path */
    if ( decode || dual )
    {
        NEXUS_AudioDecoderStartSettings decoderStartSettings;
        NEXUS_Timebase_GetSettings(NEXUS_Timebase_e0, &timebaseSettings);
        timebaseSettings.sourceType = NEXUS_TimebaseSourceType_eFreeRun;
        NEXUS_Timebase_SetSettings(NEXUS_Timebase_e0, &timebaseSettings);

        audioDecoder = NEXUS_AudioDecoder_Open(0, NULL);
        if ( NULL == audioDecoder )
        {
            fprintf(stderr, "Unable to open audio decoder 0\n");
            return -1;
        }
        NEXUS_AudioDecoder_GetDefaultStartSettings(&decoderStartSettings);
        decoderStartSettings.input = NEXUS_I2sInput_GetConnector(i2sInput);
        decoderStartSettings.codec = NEXUS_AudioCodec_ePcm;

        if (spdifHandle) {
            errCode = NEXUS_AudioOutput_AddInput(spdifHandle,
                                       NEXUS_AudioDecoder_GetConnector(audioDecoder, NEXUS_AudioConnectorType_eStereo));
            if ( errCode ) {
                fprintf(stderr, "Unable to connect Decoder -> SPDIF Output\n");
                goto cleanup;
            }
        }
        else {
            fprintf(stderr, "No SPDIF Output\n");
            goto cleanup;
        }

        NEXUS_AudioDecoder_Start(audioDecoder, &decoderStartSettings);
    }

    /* set up the direct path */
    if ( !decode || dual )
    {
        NEXUS_AudioMixerSettings mixerSettings;
        NEXUS_AudioInputCaptureStartSettings captureStartSettings;
        inputCapture = NEXUS_AudioInputCapture_Open(0, NULL);
        NEXUS_AudioInputCapture_GetDefaultStartSettings(&captureStartSettings);
        captureStartSettings.input = NEXUS_I2sInput_GetConnector(i2sInput);

        NEXUS_AudioMixer_GetDefaultSettings(&mixerSettings);
        /*mixerSettings.fixedOutputFormat = NEXUS_AudioMultichannelFormat_eStereo;*/
        mixerSettings.intermediate = true;
        intMixer = NEXUS_AudioMixer_Open(&mixerSettings);
        if ( !intMixer )
        {
            fprintf(stderr, "Unable to open Intermediate HW Mixer\n");
            goto cleanup;
        }

        NEXUS_AudioMixer_AddInput(intMixer, NEXUS_AudioInputCapture_GetConnector(inputCapture));

        if (audioDacHandle) {
            errCode = NEXUS_AudioOutput_AddInput(audioDacHandle,
                                                 NEXUS_AudioMixer_GetConnector(intMixer));
            if ( errCode ) {
                fprintf(stderr, "Unable to connect I2S Input -> DAC\n");
                goto cleanup;
            }
        }

        if ( i2sOutputHandle ) {
            errCode = NEXUS_AudioOutput_AddInput(i2sOutputHandle,
                                                 NEXUS_AudioMixer_GetConnector(intMixer));
            if ( errCode ) {
                fprintf(stderr, "Unable to connect I2S Input -> DAC\n");
                goto cleanup;
            }
        }

        #if NEXUS_HAS_HDMI_OUTPUT
        if ( audioHdmiHandle ) {
            errCode = NEXUS_AudioOutput_AddInput(audioHdmiHandle,
                                                 NEXUS_AudioMixer_GetConnector(intMixer));
            if ( errCode ) {
                fprintf(stderr, "Unable to connect I2S Input -> HDMI\n");
                goto cleanup;
            }
        }
        #endif

        NEXUS_AudioInputCapture_Start(inputCapture, &captureStartSettings);
    }

    fprintf(stderr, "Press enter to exit\n");
    getchar();

cleanup:
    if ( inputCapture )
    {
        NEXUS_AudioInputCapture_Stop(inputCapture);
    }
    if ( audioDecoder )
    {
        NEXUS_AudioDecoder_Stop(audioDecoder);
    }
    if ( audioDacHandle ) {
        NEXUS_AudioOutput_RemoveAllInputs(audioDacHandle);
    }
    if ( spdifHandle ) {
        NEXUS_AudioOutput_RemoveAllInputs(spdifHandle);
    }
    if ( intMixer )
    {
        NEXUS_AudioMixer_RemoveAllInputs(intMixer);
    }
    if ( audioDecoder )
    {
        NEXUS_AudioInput_Shutdown(NEXUS_AudioDecoder_GetConnector(audioDecoder, NEXUS_AudioConnectorType_eStereo));
    }
    if ( inputCapture )
    {
        NEXUS_AudioInput_Shutdown(NEXUS_AudioInputCapture_GetConnector(inputCapture));
    }
    NEXUS_AudioInput_Shutdown(NEXUS_I2sInput_GetConnector(i2sInput));
    if ( intMixer )
    {
        NEXUS_AudioMixer_Close(intMixer);
    }
    if ( audioDecoder )
    {
        NEXUS_AudioDecoder_Close(audioDecoder);
    }
    if ( inputCapture )
    {
        NEXUS_AudioInputCapture_Close(inputCapture);
    }
    NEXUS_I2sInput_Close(i2sInput);
    NEXUS_Platform_Uninit();

    return 0;
}

