#ifndef BMUXLIB_ASP_MEMORY_H_
#define BMUXLIB_ASP_MEMORY_H_

/* Module Overview:
 *
 * The Memory Interface is an abstraction to allow the mux manager to
 * read/write scratch device memory in DRAM. The mux manager uses this memory
 * to temporarily store data that is generated by the mux manager that needs to
 * be sent to the output module (PUSI packets, PES Headers, PCRs, etc) for TS
 * packetization.
 *
 * The buffer offset can be up to 64-bits wide
 * The buffer must have a size that is a multiple of 188
 */

typedef struct BMUXlib_ASP_Memory_Interface_t
{
   uint64_t uiOffset; /* DRAM offset of buffer used for generating PES Header, BTP packets, PCRs, etc */
   size_t uiSize; /* Must be a multiple of 188-bytes */
} BMUXlib_ASP_Memory_Interface_t;

#endif /* BMUXLIB_ASP_MEMORY_H_ */
