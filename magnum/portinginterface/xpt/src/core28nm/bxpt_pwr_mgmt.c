BERR_Code BXPT_P_Mcpb_Standby(BXPT_Handle hXpt)
{
    BERR_Code rc;
    unsigned i, j, size;
    BREG_Handle hreg = hXpt->hRegister;
#if (!BXPT_P_HAS_LONGREGS)
    uint32_t addr, *p;
#else
    uint32_t addr;
    uint64_t *p;
#endif

    size = sizeof(MCPB_TOP_REG_SAVELIST) + sizeof(MCPB_CH_REG_SAVELIST)*BXPT_NUM_PLAYBACKS;
#if BXPT_DMA_HAS_MEMDMA_MCPB
    size *= 2;
#endif
#if BXPT_P_HAS_LONGREGS /* TODO: temp solution until RDB integration */
    size *= 2;
#endif

    hXpt->mcpbBackup = BKNI_Malloc(size);
    if (hXpt->mcpbBackup==NULL) {
        rc = BERR_TRACE(BERR_OUT_OF_SYSTEM_MEMORY);
        goto error;
    }

    p = hXpt->mcpbBackup;

    for (j=0; j<MCPB_TOP_REG_SAVELIST_COUNT; j++) {
        addr = MCPB_TOP_FIRST_REG + (MCPB_TOP_REG_SAVELIST[j] - MCPB_TOP_FIRST_REG);
        *(p + j) = BXPT_P_RegIs64bit(addr) ? BREG_Read64(hreg, addr) : BREG_Read32(hreg, addr);
    }
    p += MCPB_TOP_REG_SAVELIST_COUNT;

    for (i=0; i<BXPT_NUM_PLAYBACKS; i++) {
        for (j=0; j<MCPB_CH_REG_SAVELIST_COUNT; j++) {
            addr = MCPB_CH_FIRST_REG + i*MCPB_STEPSIZE + (MCPB_CH_REG_SAVELIST[j] - MCPB_CH_FIRST_REG);
            *(p + i*MCPB_CH_REG_SAVELIST_COUNT + j) = BXPT_P_RegIs64bit(addr) ? BREG_Read64(hreg, addr) : BREG_Read32(hreg, addr);
        }
    }
    p += BXPT_NUM_PLAYBACKS*MCPB_CH_REG_SAVELIST_COUNT;

#if BXPT_DMA_HAS_MEMDMA_MCPB
    for (j=0; j<MCPB_TOP_REG_SAVELIST_COUNT; j++) {
        addr = MEMDMA_MCPB_TOP_FIRST_REG + (MCPB_TOP_REG_SAVELIST[j] - MCPB_TOP_FIRST_REG);
        *(p + j) = BXPT_P_RegIs64bit(addr) ? BREG_Read64(hreg, addr) : BREG_Read32(hreg, addr);
    }
    p += MCPB_TOP_REG_SAVELIST_COUNT;

    for (i=0; i<BXPT_NUM_PLAYBACKS; i++) {
        for (j=0; j<MCPB_CH_REG_SAVELIST_COUNT; j++) {
            addr = MEMDMA_MCPB_CH_FIRST_REG + i*MCPB_STEPSIZE + (MCPB_CH_REG_SAVELIST[j] - MCPB_CH_FIRST_REG);
            *(p + i*MCPB_CH_REG_SAVELIST_COUNT + j) = BXPT_P_RegIs64bit(addr) ? BREG_Read64(hreg, addr) : BREG_Read32(hreg, addr);
        }
    }
    p += BXPT_NUM_PLAYBACKS*MCPB_CH_REG_SAVELIST_COUNT;
#endif

    return BERR_SUCCESS;

error:
    if (hXpt->mcpbBackup) {
        BKNI_Free(hXpt->mcpbBackup);
        hXpt->mcpbBackup = NULL;
    }
    return rc;
}

void BXPT_P_Mcpb_Resume(BXPT_Handle hXpt)
{
    unsigned i, j;
    BREG_Handle hreg = hXpt->hRegister;
#if (!BXPT_P_HAS_LONGREGS)
    uint32_t addr, val, *p;
#else
    uint32_t addr;
    uint64_t val, *p;
#endif

    if (!hXpt->mcpbBackup) { return; }
    p = hXpt->mcpbBackup;

#ifdef BCHP_XPT_MCPB_CH0_CPU_ID_GRAB_CHANNEL
    for (i=0; i<BXPT_NUM_PLAYBACKS; i++) {
        BREG_Write32(hXpt->hRegister, BCHP_XPT_MCPB_CH0_CPU_ID_GRAB_CHANNEL + MCPB_STEPSIZE*i, 1);
    }
#endif

    for (j=0; j<MCPB_TOP_REG_SAVELIST_COUNT; j++) {
        addr = MCPB_TOP_FIRST_REG + (MCPB_TOP_REG_SAVELIST[j] - MCPB_TOP_FIRST_REG);
        val = *(p + j);
        BXPT_P_RegIs64bit(addr) ? (void)BREG_Write64(hreg, addr, val) : (void)BREG_Write32(hreg, addr, val);
    }
    p += MCPB_TOP_REG_SAVELIST_COUNT;

    for (i=0; i<BXPT_NUM_PLAYBACKS; i++) {
        for (j=0; j<MCPB_CH_REG_SAVELIST_COUNT; j++) {
            addr = MCPB_CH_FIRST_REG + i*MCPB_STEPSIZE + (MCPB_CH_REG_SAVELIST[j] - MCPB_CH_FIRST_REG);
            val = *(p + i*MCPB_CH_REG_SAVELIST_COUNT + j);
            BXPT_P_RegIs64bit(addr) ? (void)BREG_Write64(hreg, addr, val) : (void)BREG_Write32(hreg, addr, val);
        }
    }
    p += BXPT_NUM_PLAYBACKS*MCPB_CH_REG_SAVELIST_COUNT;

#if BXPT_DMA_HAS_MEMDMA_MCPB
    for (j=0; j<MCPB_TOP_REG_SAVELIST_COUNT; j++) {
        addr = MEMDMA_MCPB_TOP_FIRST_REG + (MCPB_TOP_REG_SAVELIST[j] - MCPB_TOP_FIRST_REG);
        val = *(p + j);
        BXPT_P_RegIs64bit(addr) ? (void)BREG_Write64(hreg, addr, val) : (void)BREG_Write32(hreg, addr, val);
    }
    p += MCPB_TOP_REG_SAVELIST_COUNT;

    for (i=0; i<BXPT_NUM_PLAYBACKS; i++) {
        for (j=0; j<MCPB_CH_REG_SAVELIST_COUNT; j++) {
            addr = MEMDMA_MCPB_CH_FIRST_REG + i*MCPB_STEPSIZE + (MCPB_CH_REG_SAVELIST[j] - MCPB_CH_FIRST_REG);
            val = *(p + i*MCPB_CH_REG_SAVELIST_COUNT + j);
            BXPT_P_RegIs64bit(addr) ? (void)BREG_Write64(hreg, addr, val) : (void)BREG_Write32(hreg, addr, val);
        }
    }
    p += BXPT_NUM_PLAYBACKS*MCPB_CH_REG_SAVELIST_COUNT;
#endif

    BKNI_Free(hXpt->mcpbBackup);

#ifdef BCHP_XPT_MCPB_CH0_CPU_ID_CFG_DONE
    for (i=0; i<BXPT_NUM_PLAYBACKS; i++) {
        BREG_Write32(hXpt->hRegister, BCHP_XPT_MCPB_CH0_CPU_ID_CFG_DONE + MCPB_STEPSIZE*i, 1);
    }
#endif
}
