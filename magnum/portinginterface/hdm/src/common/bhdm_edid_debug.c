const uint8_t * BHDM_EDID_P_GetDebugEdid(const BHDM_Handle hHDMI)
{
	uint16_t i ;
	uint8_t index = hHDMI->DeviceSettings.uiDebugEdid ;

	if (index == 5)
	{
		return DebugRxTestEdid ;
	}
	else if ((index == 3) || (index == 4))
	{
		for (i = 0 ; i < BHDM_EDID_BLOCKSIZE * 2 ; i++)
		{
			DebugRxTestEdid[i] = (index == 4) ? 0xff : 0x00 ;
		}
		return DebugRxTestEdid ;
	}

	return DebugRxEdid[index] ;
}


